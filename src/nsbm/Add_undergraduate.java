        ugd.setGender(ddgender.getSelectedItem().toString());
        ugd.setEmail(txtEmail.getText());
        ugd.setContactNo(Integer.parseInt(txtContactNo.getText()));
        ugd.setAlSubject1(txtalsubject1.getText());
        //System.out.println("a");
        ugd.setSubject_1(ddalsubject1.getSelectedItem().toString());
        ugd.setAlSubject2(txtalsubject2.getText());
        ugd.setSubject_2(ddalsubject2.getSelectedItem().toString());
        ugd.setAlSubject3(txtalsubject3.getText());
        ugd.setSubject_3(ddalsubject3.getSelectedItem().toString());

        ugd.setZScore(txtZScore.getText());
        //System.out.println("aaaaabb");
        ugd.setRegYear(ddYear.getSelectedItem().toString());
        //System.out.println("aaaaa");
        ugd.setDegree(ddDegree.getSelectedItem().toString());
        //System.out.println("aaaaab");
        ugd.setFaculty(ddFaculty.getSelectedItem().toString());
        //System.out.println("aa");
        
        boolean result=dbops.underGraduateStudent(ugd);

        if(result){
            JOptionPane.showMessageDialog(this,"Successfully Inserted ....!");
            clearField();
            //this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this,"Sorry... Inserted Error");
            clearField();
        }
