        ugd.setNamewithInitial(txtNameWithInitial.getText());
        ugd.setNic(txtNic.getText());
        ugd.setBirthYear(ddbirthyear.getSelectedItem().toString());
        ugd.setBirthMonth(ddmonth.getSelectedItem().toString());
        ugd.setBIrthDate(ddday.getSelectedItem().toString());
